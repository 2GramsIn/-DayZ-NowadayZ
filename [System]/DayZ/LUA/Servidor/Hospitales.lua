function Hospitales.Refrescar()
	for k, v in ipairs(Hospitales) do
		for _, Item in ipairs(Medicinas) do
			v:setData(Item, math.random(1, 5))
		end
	end
end
setTimer(Hospitales.Refrescar, 3600000, 0)

function Hospitales.Crear()
	for k, v in ipairs(Hospitales.Cajas) do
		createObject(1558, v[1], v[2], v[3])

		Hospitales[k] = createColSphere(v[1], v[2], v[3], 2)
		Hospitales[k]:setData('hospitalbox', true)
		Hospitales[k] :setData('MAX_Slots', 0)
	end
	
	Hospitales.Refrescar()
end
addEventHandler('onResourceStart', resourceRoot, Hospitales.Crear)